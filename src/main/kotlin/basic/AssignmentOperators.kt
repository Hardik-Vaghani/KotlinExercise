/*

ASSIGNMENT OPERATORS

a +=b	a = a + b
a += b	a = a + b
a *= b	a = a * b
a /= b	a = a / b
a %= b	a = a % b

 */


fun main() {

    var a = 10

    val b = 5

    val sonuc: Int = a + b  // The sum of a and b values was assigned to the result variable.

    println(sonuc) // the result variable was found to be 15.

    a += b // The value b was added to the value a and assigned to the value a.

    println(a) // a result is 15
}