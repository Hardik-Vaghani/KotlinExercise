/*
pre-increment: ++a // increase the value first and then perform the operation.
front decrement: --a // first decrement the value and then perform the operation.
rear increment: a++ // do the operation first, then increment
front decrement: a-- // do the operation first, then decrement
-a : makes the element negative.
+a : makes the element positive.
!a : Not equal to a
*/


fun main() {

    var number = 5 // variable defined.
    println(number) // value displayed on screen.

    ++number // number value increased by 1.
    println(number) //value displayed on screen.

    number-- // number value decreased by 1.
    println(number) // The value was shown on the screen.


}